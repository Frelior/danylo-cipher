export function textToChifer(text) {
  const textArr = text.toLowerCase().split("")
  const chiferArr = textArr.map((letter) => {
    return alphabet[letter]
  })
  const result = chiferArr.join(" ")
  return result
}

export function chiferToText(text) {
  const textArr = text.split(" ")
  const chiferArr = textArr.map((letter) => {
    return Object.keys(alphabet).find((key) => alphabet[key] === letter)
  })
  const result = chiferArr.join("")
  return result
}

